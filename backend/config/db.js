// WHY THIS FILE EXISTS:
// Database connection logic is isolated here so server.js stays clean.
// If you ever switch databases (MongoDB ‚Üí PostgreSQL) you only touch this file.
// Also makes it easy to test the connection independently.
//
// FLOW:
//   mongoose.connect() returns a Promise.
//   We await it in server.js: connectDB().then(() => app.listen(...))
// ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ

const mongoose = require("mongoose");

const connectDB = async () => {
    // mongoose.connect() takes the URI and an options object.
    // The URI comes from .env ‚Äî never hardcode it here.
    const conn = await mongoose.connect(process.env.MONGO_URI, {
        // These options suppress deprecation warnings in newer Mongoose versions:
        // (Mongoose 6+ sets them by default, but explicit is better for beginners)
    });

    console.log(`‚úÖ MongoDB connected: ${conn.connection.host}`);

    // ‚îÄ‚îÄ Connection event listeners ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
    // These fire AFTER the initial connection ‚Äî useful for catching issues
    // that happen mid-runtime (e.g., network drops).

    mongoose.connection.on("disconnected", () => {
        console.warn("‚ö†Ô∏è  MongoDB disconnected. Attempting to reconnect...");
    });

    mongoose.connection.on("reconnected", () => {
        console.log("üîÑ MongoDB reconnected successfully.");
    });

    mongoose.connection.on("error", (err) => {
        console.error("‚ùå MongoDB runtime error:", err.message);
    });

    return conn;
};

module.exports = connectDB;
